public class EntityPaper {
    
    private String author;
    private String affiliation;
    private String subject;
    private String datasetversion_id;

    /**
     * @return the author
     */
    public String getAuthor() {
        return author;
    }

    /**
     * @param author the author to set
     */
    public void setAuthor(String author) {
        this.author = author;
    }

    /**
     * @return the affiliation
     */
    public String getAffiliation() {
        return affiliation;
    }

    /**
     * @param affiliation the affiliation to set
     */
    public void setAffiliation(String affiliation) {
        this.affiliation = affiliation;
    }

    /**
     * @return the subject
     */
    public String getSubject() {
        return subject;
    }

    /**
     * @param subject the subject to set
     */
    public void setSubject(String subject) {
        this.subject = subject;
    }

    /**
     * @return the datasetversion_id
     */
    public String getDatasetversion_id() {
        return datasetversion_id;
    }

    /**
     * @param datasetversion_id the datasetversion_id to set
     */
    public void setDatasetversion_id(String datasetversion_id) {
        this.datasetversion_id = datasetversion_id;
    }
    
    
    
    
    
}
